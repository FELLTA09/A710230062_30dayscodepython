# Angka yg di gunakan
angka1 = 10
angka2 = 5

# Penjumlahan
hasil_penjumlahan = angka1 + angka2
print(f"Hasil penjumlahan {angka1} + {angka2} = {hasil_penjumlahan}")

# Pengurangan
hasil_pengurangan = angka1 - angka2
print(f"Hasil pengurangan {angka1} - {angka2} = {hasil_pengurangan}")

# Perkalian
hasil_perkalian = angka1 * angka2
print(f"Hasil perkalian {angka1} * {angka2} = {hasil_perkalian}")

# Pembagian
hasil_pembagian = angka1 / angka2
print(f"Hasil pembagian {angka1} / {angka2} = {hasil_pembagian}")
